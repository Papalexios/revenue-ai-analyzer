const capitalize = (s: string) => {
    if (s === 'ai') return 'AI';
    if (s === 'seo') return 'SEO';
    return s.charAt(0).toUpperCase() + s.slice(1);
}

const formatTitle = (slug: string): string => {
    return slug.split('-').map(capitalize).join(' ');
};

const getCategory = (path: string): string => {
    const parts = path.split('/').filter(p => p);
    if (parts.length > 1) {
        const potentialCategory = parts[0];
        const categories: { [key: string]: string } = {
            'seo': 'SEO',
            'ai': 'AI',
            'blogging': 'Blogging',
            'affiliate-marketing': 'Affiliate Marketing',
            'review': 'Reviews',
            'how-to-start': 'Getting Started',
            'email-marketing': 'Email Marketing',
            'tools': 'Tools',
            'web-hosting': 'Web Hosting',
            'make-money-online': 'Make Money Online',
            'info': 'Info',
            'chatgpt-prompts': 'ChatGPT Prompts',
        };
        if (categories[potentialCategory]) {
            return categories[potentialCategory];
        }
    }
    return 'General';
};


export const resources: ResourceLink[] = rawUrls.map(url => {
    try {
        const urlObj = new URL(url);
        const path = urlObj.pathname;
        const pathParts = path.split('/').filter(p => p);
        // Use the second to last part if the last part is empty (e.g., from a trailing slash)
        const slug = pathParts[pathParts.length - 1] || pathParts[pathParts.length - 2] || 'resource';

        return {
            url,
            title: formatTitle(slug),
            category: getCategory(path),
        };
    } catch(e) {
        console.error(`Could not parse URL: ${url}`, e);
        return {
            url,
            title: 'Invalid URL',
            category: 'Error',
        }
    }
}).filter(r => r.category !== 'Error');

export const categories = [...new Set(resources.map(r => r.category))].sort();
